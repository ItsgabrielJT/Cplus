#include <iostream>
#include <cmath>
using namespace std;

int main()
{
    // 1.- ceil()
    // redonde x al valor mas éuqeño que son sea x
    cout << ceil(9.2) << endl;
    cout << ceil(9.8) << endl;
    
    // 2.- Funciones trigonometricas
    cout << sin(90) << endl;
    cout << cos(0) << endl;
    cout << tan(45) << endl;

    // 3.- Raices cuadradas
    cout << sqrt(144) << endl;

    // 4.- Potencias
    // x eleveada a la y
    cout << pow(2, 3) << endl;

    
    system("pause");
    return 0;
}